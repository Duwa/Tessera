"""
HACM Onboarding / Offboarding Service  —  port 8007
=====================================================
Manages the full join/leave lifecycle for human employees AND AI agents.

Odoo hr_onboarding covers:
    Checklists, document collection, system access provisioning,
    welcome emails, buddy assignment, probation tracking.

HACM extends it with:
    - Belief fit assessment at entry (vs org template)
    - Token budget provisioning for day one
    - AI agent onboarding: mandate declaration, guardrail setup,
      RAG seeding from org_memory, belief vector initialization
    - Learning plan generation on day one (from learning service)
    - Offboarding: knowledge capture before departure,
      directed mutation trigger (T_replace_h2a or T_probe_entry),
      AI agent retirement: mandate archival, memory cleanup

Key difference from Odoo:
    Odoo onboards humans. HACM onboards humans AND AI agents,
    and treats both as capital units entering or leaving the org.

    When a human leaves: T_probe_entry may be triggered (outsider injection).
    When an AI agent is retired: correlated failure record is archived,
    and T_replace_a2h may reinstate human oversight in that domain.
"""

