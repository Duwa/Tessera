# ── BUSINESS LOGIC ────────────────────────────────────────────────────

def _build_task_list(unit_type: UnitType, journey_type: JourneyType,
                     custom_tasks: Optional[List[dict]] = None) -> List[dict]:
    """Assemble task list from template for this unit type + journey type."""
    template_map = {
        (UnitType.HUMAN, JourneyType.ONBOARDING):    HUMAN_ONBOARDING_TASKS,
        (UnitType.HUMAN, JourneyType.OFFBOARDING):   HUMAN_OFFBOARDING_TASKS,
        (UnitType.AI_AGENT, JourneyType.ONBOARDING): AI_AGENT_ONBOARDING_TASKS,
        (UnitType.AI_AGENT, JourneyType.OFFBOARDING):AI_AGENT_OFFBOARDING_TASKS,
    }
    tasks = [
        {**t, "status": TaskStatus.PENDING.value, "completed_by": None,
         "completed_at": None, "notes": None}
        for t in template_map.get((unit_type, journey_type), [])
    ]
    if custom_tasks:
        tasks.extend([{**t, "status": TaskStatus.PENDING.value,
                       "completed_by": None, "completed_at": None, "notes": None}
                      for t in custom_tasks])
    return sorted(tasks, key=lambda t: t.get("day", 0))


def _compute_progress(tasks: List[dict]) -> dict:
    total = len(tasks)
    required = [t for t in tasks if t.get("required", True)]
    done = [t for t in tasks if t["status"] == TaskStatus.COMPLETE.value]
    req_done = [t for t in required if t["status"] == TaskStatus.COMPLETE.value]
    return {
        "total_tasks": total,
        "completed": len(done),
        "required_total": len(required),
        "required_completed": len(req_done),
        "pct_complete": round(len(done) / max(total, 1) * 100, 1),
        "required_pct": round(len(req_done) / max(len(required), 1) * 100, 1),
        "is_complete": len(req_done) == len(required),
    }


def _activation_velocity(journey: dict) -> dict:
    """
    Estimate activation velocity — how quickly is this unit reaching
    productive contribution? HACM CPM S1 (H+AI Activation Velocity).
    """
    progress = journey["progress"]
    days_in = (date.today() - date.fromisoformat(journey["start_date"])).days
    unit_type = journey["unit_type"]

    # Benchmarks from Rajendra (2026) simulation
    benchmarks = {
        "human":    {"day_1_target": 40, "day_7_target": 65, "day_30_target": 85},
        "ai_agent": {"day_1_target": 80, "day_7_target": 90, "day_30_target": 95},
    }
    bench = benchmarks.get(unit_type, benchmarks["human"])

    current = progress["required_pct"]
    if days_in <= 1:
        target = bench["day_1_target"]
    elif days_in <= 7:
        target = bench["day_7_target"]
    else:
        target = bench["day_30_target"]

    on_track = current >= target
    return {
        "days_in": days_in,
        "current_progress_pct": current,
        "target_pct": target,
        "on_track": on_track,
        "velocity_status": "on track" if on_track else "behind",
        "note": (
            f"{'AI agents activate faster than humans — '  if unit_type == 'ai_agent' else ''}"
            f"{'✓ Activation on track.' if on_track else f'⚠ Behind target. Focus on required tasks first.'}"
        ),
    }


