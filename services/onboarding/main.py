@app.get("/summary")
def org_journey_summary():
    journeys = list(_journeys.values())
    active = [j for j in journeys if j["state"] == JourneyState.IN_PROGRESS.value]
    complete = [j for j in journeys if j["state"] == JourneyState.COMPLETE.value]
    human_onboarding = [j for j in active
                         if j["unit_type"] == "human" and j["journey_type"] == "onboarding"]
    ai_onboarding = [j for j in active
                      if j["unit_type"] == "ai_agent" and j["journey_type"] == "onboarding"]
    offboarding = [j for j in active if j["journey_type"] == "offboarding"]
    return {
        "total_journeys": len(journeys),
        "active": len(active),
        "complete": len(complete),
        "human_onboarding_active": len(human_onboarding),
        "ai_agent_onboarding_active": len(ai_onboarding),
        "offboarding_active": len(offboarding),
        "mutations_triggered": [
            j["mutation_trigger"] for j in journeys if j.get("mutation_trigger")
        ],
    }


# ── HACM INSIGHT ──────────────────────────────────────────────────────

def _onboarding_insight(req: JourneyStartRequest) -> dict:
    if req.journey_type == JourneyType.ONBOARDING:
        if req.unit_type == UnitType.AI_AGENT:
            return {
                "title": "AI agent activation begins at day 0",
                "message": (
                    f"Unlike human onboarding (30+ days to full activation), "
                    f"{req.unit_name} begins Regime C operational learning immediately. "
                    f"Token budget must be above B* threshold from day one for positive net fitness."
                ),
                "mutation_note": "T_probe_entry is NOT triggered — this is a new AI agent deployment, not a human outsider injection.",
            }
        else:
            belief_note = ""
            if req.belief_alignment_at_entry is not None:
                if req.belief_alignment_at_entry < 0.5:
                    belief_note = f" Belief alignment at entry is {req.belief_alignment_at_entry:.0%} — below average. Schedule belief conversation early."
                else:
                    belief_note = f" Belief alignment at entry is {req.belief_alignment_at_entry:.0%} — good starting point."
            return {
                "title": "Human onboarding activates the T_probe_entry pattern",
                "message": (
                    f"{req.unit_name} joins as an outsider — their belief vector seeds from an external distribution, "
                    f"not from org_memory. This is deliberate: outsiders inject epistemic diversity the org cannot generate internally."
                    + belief_note
                ),
                "mutation_note": "T_probe_entry fires at scheduled interval. This onboarding is part of that mechanism.",
            }
    else:
        return {
            "title": f"Offboarding triggers knowledge capture and mutation review",
            "message": (
                f"{'Mandate archival and human oversight review required.' if req.unit_type == UnitType.AI_AGENT else 'Tacit knowledge must be captured before departure. Transmission failure is a structural risk (Rajendra §3.4).'}"
            ),
            "mutation_note": f"Mutation trigger: {req.mutation_trigger.value if req.mutation_trigger else 'not yet determined — review required'}",
        }


